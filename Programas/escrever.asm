APAGA	PROC
	MOV CX,20	;20 LINHAS EU QUERO APAGAR
DENOVO2:
	LEA BP,SPACE
	MOV BH,0
	MOV BL,0FH
	PUSH CX
	MOV DL,0	;COLUMN NUMBER
	MOV DH,CL	;ROW NUMBER
	DEC DH
	MOV CX,80	; COMPRIMENTO DA LINHA
	MOV AH,13H	
	MOV AL,1	;MODO DE IMPRIMIR CARACTERES E NAO ATRIBUTOS
	INT 10H
	POP CX
	LOOP DENOVO2
	RET

